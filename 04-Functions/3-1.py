# To use the functions available in an external module,
# you must include that module in your program.
import math

square_root = math.sqrt(7)
print('A square root of 7 is', square_root)

natural_logarithm = math.log1p(4)
print('The natural logarithm of 5 is', natural_logarithm)

angle_in_radians = math.radians(90)
sine = math.sin(angle_in_radians)
print('The sine of 90 degrees is', sine)